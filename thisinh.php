    $(document).on('ready', function() {
        $(".regular").slick({
            dots: true,
            infinite: true,
            slidesToShow: 3,
            slidesToScroll: 3,
            slidesToShow: 1,
            slidesToScroll: 1,
            autoplay: true,
            speed: 500
        });
